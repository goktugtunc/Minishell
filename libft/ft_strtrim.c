#include "libft.h"

char	*ft_strtrim(char *s1, char const *set)
{
	size_t	i;
	char	*returnvalue;

	if (!s1 || !set)
		return (0);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	i = ft_strlen(s1);
	while (i && ft_strchr(set, s1[i]))
		i--;
	returnvalue = ft_strdup(ft_substr(s1, 0, i + 1));
	free(s1);
	return (returnvalue);
}
